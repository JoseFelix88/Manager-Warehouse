
package com.managerwarehouse.model.contabilidad.activofijo;


public class Categoria {
    
    private int itemcategoria;
    private String descripcion;
    private String prefijo;
    private String codigoPuc;

    public int getItemcategoria() {
        return itemcategoria;
    }

    public void setItemcategoria(int itemcategoria) {
        this.itemcategoria = itemcategoria;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getPrefijo() {
        return prefijo;
    }

    public void setPrefijo(String prefijo) {
        this.prefijo = prefijo;
    }

    public String getCodigoPuc() {
        return codigoPuc;
    }

    public void setCodigoPuc(String codigoPuc) {
        this.codigoPuc = codigoPuc;
    }
    
    
}
