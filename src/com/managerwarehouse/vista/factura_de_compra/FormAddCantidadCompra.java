    private void txtembalajeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtembalajeActionPerformed
        // TODO add your handling code here:

        if (Variables_Gloabales.EMPLEADO.getNivel() != 2) {
            txtundcompradas.grabFocus();
            Edicion e = new Edicion();
            int unda = e.toNumeroEntero(txtcantcajas.getText())  * e.toNumeroEntero(txtembalaje.getText());
            txtundcompradas.setText(""+unda);
        } else {
            txtcostocaja.selectAll();
            txtcostocaja.requestFocus();
        }

    }//GEN-LAST:event_txtembalajeActionPerformed

